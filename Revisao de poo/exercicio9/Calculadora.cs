class Calculadora
{
    public static int Somar(int a, int b)
    {
        Console.WriteLine($"resultado da soma é igual {a + b}");
        
        return a + b;
    }

    public static int Multiplicar(int a, int b)
    {
        Console.WriteLine($"resultado da multiplicação é igual {a * b}");
        return a * b;
    }
}
